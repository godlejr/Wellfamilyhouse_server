package com.demand.server.well_family_house.dto;

public class CheckBox {
	private int checked;
	
	public CheckBox() {
		super();
	}

	public CheckBox(int checked) {
		super();
		this.checked = checked;
	}

	public int getChecked() {
		return checked;
	}

	public void setChecked(int checked) {
		this.checked = checked;
	}
	
	
}
